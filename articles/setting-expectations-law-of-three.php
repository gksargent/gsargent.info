          <p>When you're a freelancer, it's so important to set the right expectations as early as possible with each client. This blog post is the start of a series called "Setting Expectations with Clients" that I'll be working on to help you avoid the costly mistakes I made over and over.</p>

          <p>There was once a time, a few years ago, where I wanted to far exceed all of my clients expectations. I thought being super responsive and readily available would help me keep their business long-term and possibly get me in touch with some of their friends. I would spend an hour or two on the phone with clients, letting them end the conversation when they were done asking questions. </p>

          <p>But eventually, the same thing happened every time, with every client - I couldn't sustain that level of communication because I had projects to work on and new clients to talk to. So I would take shorter phone calls and take longer to communicate with that client. And not very surprisingly, those clients that I had spent hours on the phone with a few weeks before were upset that I didn't have time to talk to them now.</p>

          <blockquote>The Law of Three has changed the way I interact with my clients.</blockquote>

          <p>It took some time and some thinking to avoid this problem, but I've had better success keeping clients happy long-term. It's called The Law of Three and it has changed the way I interact with my clients.</p>

          <p>The Law of Three is a way to set the right expectations in the first three points of communication you have with any client. This can be the first three emails, phone calls, texts, etc. Whichever mode of communication you use, the main focus is on the first three times you respond to the client.</p>

          <h2>Here's a few hypothetical scenarios to consider:</h2>

          <h3>Initial communication</h3>
          <p>In the first three times you replied or talked to the client, how long did you wait to reply or make yourself available? Did you hear the ding of an email notification and respond immediately out of pure excitement, or did you wait a little while? </p>

          <p>The Law of Three takes the average of the time the client had to wait in order to get a reply from you. If you respond quickly in the first three interactions, then the client will expect immediate responses from you in the months ahead, so tread carefully with this one!</p>

          <h3>How long you communicate</h3>
          <p>In the first three interactions with the client, did you talk for hours to get to know each other, or did you have short conversations that were straight to the point and quick? This also applies to emails - were your replies a few sentences or a few pages worth?</p>

          <p>The Law of Three averages the amount of time you dedicated to the first three interactions and sets the client's expectations of future communication. If you averaged an hour for the first three phone calls, then the client will assume that future phone calls will also take an hour. And as someone who pretty much hates talking on the phone for any length of time, I can assure you that learning how to keep phone calls short and to the point was one of the best things ever!</p>

          <h3>How you communicate</h3>
          <p>In the first three interactions, did you take phone calls even though you do all of your communication via email? Or maybe you use a tool like Basecamp or Evernote and your client has no idea because you've been replying to their emails?</p>

          <p>The Law of Three determines the mode of communication in the first three interactions, and helps you set the expectation that future communication will be done via Basecamp/Evernote/Email/Phone calls etc. You're the boss, you decide. But make sure you make it clear within the first three points of communication so the expectation was made early on.</p>

          <blockquote>Use the Law of Three to set clear expectations within the first three points of communication so you can have a healthy and happy relationship with your client.</blockquote>

          <p>The last thing you want is to start getting frustrated with a client that doesn't understand your preferences and is unknowingly getting on your nerves. Use the Law of Three to set clear expectations within the first three points of communication so you can have a healthy and happy relationship with your client. They hired you for a reason, so now the process is in your hands and you get to decide how your business is run.</p>

          <p>Remember, it's your business and the client assumes that you have a system in place to make things run smoothly. So take some time to figure out your preferences so you're ready to make use of the Law of Three with your next client!</p>


